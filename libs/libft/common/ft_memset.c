#include "libft_common.h"

/**
 * The ft_memset() function fills the first n bytes of the
 * memory area pointed to by s with the constant byte c.
 *
 * @param	{void*}		s	The memory area to be filled.
 * @param	{int}		c	The filling value.
 * @param	{size_t}	n	The first n bytes of s.
 *
 * @returns	{void*}		s	The filled memory area.
*/
void	*ft_memset(void *s, int c, size_t n)
{
	size_t	i;

	i = 0;
	while (i < n)
	{
		*((unsigned char *)s + i) = (unsigned char)c;
		i++;
	}
	return (s);
}
